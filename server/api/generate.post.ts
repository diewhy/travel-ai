import OpenAI from 'openai'

export default defineEventHandler(async (event) => {
  const body = await readBody(event)
  const config = useRuntimeConfig()

  const openai = new OpenAI({
    apiKey: config.openaiApiKey
  })

  const response = await openai.responses.create({
    model: 'gpt-4.1-mini',
    max_output_tokens: 2200,
    temperature: 0.7,
    input: `
Ты — AI-помощник для путешествий по России.

Составь понятный и полезный маршрут путешествия.

Данные пользователя:
Место: ${body.place}
Количество дней: ${body.days}
Бюджет: ${body.budget} рублей
Тип отдыха: ${body.travelType}

Требования:
- Ответь только на русском языке.
- Не пиши вступление.
- Не выдумывай слишком фантастические места.
- Учитывай бюджет.
- Маршрут должен быть реалистичным.
- Каждый день должен быть отдельным блоком.
- В каждом дне укажи места, примерные расходы и совет.
- В конце укажи примерный итоговый бюджет.
- Пиши красиво, но кратко.

Формат строго такой:

**День 1 — название дня**
📍  Места:
- место 1
- место 2

💰 Расходы:
- транспорт: примерно ... ₽
- питание: примерно ... ₽
- активности: примерно ... ₽

💡  Совет:
краткий совет

**День 2 — название дня**
📍  Места:
- место 1
- место 2

💰 Расходы:
- транспорт: примерно ... ₽
- питание: примерно ... ₽
- активности: примерно ... ₽

💡  Совет:
краткий совет

**Итоговый бюджет**
примерно ... ₽

**Общий совет**
краткий совет для поездки
`
  })

  return {
    route: response.output_text
  }
}) 